export function detectLanguage(): LanguageCode {
  const language = window.navigator.language.slice(0, 2).toLowerCase();
  return isLanguageCode(language) ? language : 'en';
}

export function isLanguageCode(value: string): value is LanguageCode {
  return SUPPORTED_LANGUAGES.includes(value as LanguageCode);
}

export function t(language: LanguageCode, key: TranslationKey): string {
  return STRINGS[language]?.[key] ?? EN_STRINGS[key];
}

export function tf(language: LanguageCode, key: TranslationKey, values: Record<string, string | number>): string {
  return t(language, key).replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

export function powerUpLabelKey(type: string): TranslationKey {
  switch (type) {
    case 'magnet':
      return 'powerupMagnet';
    case 'shrink':
      return 'powerupShrink';
    case 'haste':
      return 'powerupHaste';
    case 'shield':
      return 'powerupShield';
    case 'stamina':
      return 'powerupStamina';
    case 'mass':
    default:
      return 'powerupMass';
  }
}
